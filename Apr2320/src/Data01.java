/*주석문이란?
 * 주석문이란 소스에 대한 설명문으로 실행되지 않는다.
 * // : 한줄 주석문
 *  /*: 여러 줄 주석문 
 */
public class Data01 { //class 예약어 (키워드)로 Data01 클래스명 정의 

	public static void main(String[] args) {
    //void : 반환값이 없는 타입 
		
		System.out.println(10); //System.out : 모니터 출력장치와 연결, print() : 출력, ln : 개행
		System.out.println(10.5);
		System.out.println('a'); //단일문자 a : 십진수 정수값으로 저장
		System.out.println(true);
		System.out.println(13L); // long 정수 자료형 값 뒤에는 접미사 L (l -> 영문소문자)온다
		System.out.println(10.5F);//float 타입 : 실수 숫자값 뒤에는 접미사 F(f) 온다
		System.out.println("홍길동");//문자열 참조타입
		

		
		
		
	}

}
