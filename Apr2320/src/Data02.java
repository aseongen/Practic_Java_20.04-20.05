/* 확장 특수문자 (이스케이프 문자)종류
 *  \n : 줄바꿈(개행)
 *  \t : 탭키만큼 수평으로 띄움
 *  \" : "큰따옴표로 출력
 *  \' : 작은따옴표 출력
 *  \\ : \\(폴더 경로 구분기호)표시
 *  \r : 커서를 맨 앞으로 옮김  
 */


public class Data02 {
	public static void main(String[] args) {

		System.out.println("\'서울시\'");
		System.out.println("\"부산시 \"");
		System.out.println("abc\tcdg\rge");
		System.out.println("\n");
		System.out.println("d:\\");             
		
		
	}
}
